number = int(input("Enter any number: "))

if number % 2 == 0 and number % 3 == 0:
    print("This number is divisible by 2 and 3!")
else:
    print("This is a wrong number!")

# Is it divisible by 5 and 6
if number % 5 == 0 and number % 6 == 0:
    print("This number is divisible by 5 and 6!")
else:
    print("This is a wrong number!")
